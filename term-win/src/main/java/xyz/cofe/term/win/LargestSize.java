package xyz.cofe.term.win;

public class LargestSize {
    public LargestSize(int width, int height) {
        this.width = width;
        this.height = height;
    }

    private final int width;

    public int getWidth() {
        return width;
    }

    private final int height;

    public int getHeight() {
        return height;
    }
}
